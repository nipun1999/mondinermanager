    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            switch (position){
                case 0:
                    AllTables tab1 = new AllTables();
                    return tab1;
                case 1:
                    confirmTables tab2 = new confirmTables();
                    return tab2;
                case 2:
                    callwaiter tab3 = new callwaiter();
                    return tab3;

                default:
                    return null;
            }
        }

            @Override
            public int getCount () {
                // Show 3 total pages.
                return 3;
            }

        }
